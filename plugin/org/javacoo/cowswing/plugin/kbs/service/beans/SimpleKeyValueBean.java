package org.javacoo.cowswing.plugin.kbs.service.beans;

/**
 * 简单键值对对象
 * <p>说明:</p>
 * <li></li>
 * @author DuanYong
 * @since 2013-7-17 上午10:05:40
 * @version 1.0
 */
public class SimpleKeyValueBean {
	public SimpleKeyValueBean() {
	}
	/**
	 * @param key
	 * @param value
	 */
	public SimpleKeyValueBean(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}
	/**键*/
	private String key;
	/**值*/
	private String value;
	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}
	/**
	 * @param key the key to set
	 */
	public void setKey(String key) {
		this.key = key;
	}
	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return  value ;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SimpleKeyValueBean other = (SimpleKeyValueBean) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}
	
	

}
